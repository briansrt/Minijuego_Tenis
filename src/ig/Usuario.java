    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        if ("".equals(id.getText()) || ("".equals(nombre.getText()))) {
            JOptionPane.showMessageDialog(null, "Todos los campos deben ser diligenciados");
        }else{
            verificador = true;
            dispose();
            game.setPlayerName(nombre.getText());
            
        }
    }//GEN-LAST:event_GuardarActionPerformed
    
    
    
    private void nombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nombreKeyTyped
        int key = evt.getKeyChar();
        boolean letrasmayus = key >=65 && key <=90;
        boolean letrasminus = key >=97 && key <=122;
        if (!(letrasmayus || letrasminus))
        evt.consume();
    }//GEN-LAST:event_nombreKeyTyped

    private void idKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idKeyTyped
        int key = evt.getKeyChar();
        boolean numeros = key >= 48 && key <=57;
        if((!numeros) || id.getText().length()>= 10)
        evt.consume();
    }//GEN-LAST:event_idKeyTyped

